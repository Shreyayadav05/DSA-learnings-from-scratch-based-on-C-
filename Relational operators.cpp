#include <bits/stdc++.h>
using namespace std;

int main() {
    int a = 10, b = 20;
    
    cout << (a > b) << endl;  // Output: 0 (false)
    cout << (a < b) << endl;  // Output: 1 (true)
    cout << (a == b) << endl; // Output: 0 (false)
    cout << (a != b) << endl; // Output: 1 (true)
    cout << (a >= b) << endl; // Output: 0 (false)
    cout << (a <= b) << endl; // Output: 1 (true)
    
    return 0;
}
