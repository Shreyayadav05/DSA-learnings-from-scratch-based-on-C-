#include <bits/stdc++.h>
using namespace std;

int main() {
    int a = 10, b = 3;
    
    cout << "Addition: " << (a + b) << endl;       // Output: 13
    cout << "Subtraction: " << (a - b) << endl;    // Output: 7
    cout << "Multiplication: " << (a * b) << endl; // Output: 30
    cout << "Division: " << (a / b) << endl;       // Output: 3
    cout << "Modulus: " << (a % b) << endl;        // Output: 1
    
    return 0;
}
